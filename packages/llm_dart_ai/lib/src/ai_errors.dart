/// Thrown when one or more tool-call ids have no corresponding tool-result.
///
/// Mirrors Vercel AI SDK's `MissingToolResultsError`.
class MissingToolResultsError extends LLMError {
  final List<String> toolCallIds;

  MissingToolResultsError({
    required List<String> toolCallIds,
    String? message,
  })  : toolCallIds = List<String>.unmodifiable(toolCallIds),
        super(
          message ??
              'Tool result${toolCallIds.length > 1 ? 's are' : ' is'} missing for tool call${toolCallIds.length > 1 ? 's' : ''} '
                  '${toolCallIds.join(', ')}.',
        );

  @override
  String toString() => 'Missing tool results: $message';
}

