/// Thrown when the model tries to call a tool that is not available.
///
/// Mirrors Vercel AI SDK's `NoSuchToolError`.
class NoSuchToolError extends LLMError {
  final String toolName;
  final List<String>? availableTools;

  NoSuchToolError({
    required this.toolName,
    this.availableTools,
    String? message,
  }) : super(message ?? _defaultNoSuchToolMessage(toolName, availableTools));

  static String _defaultNoSuchToolMessage(
    String toolName,
    List<String>? availableTools,
  ) {
    final suffix = availableTools == null
        ? 'No tools are available.'
        : 'Available tools: ${availableTools.join(', ')}.';
    return "Model tried to call unavailable tool '$toolName'. $suffix";
  }

  @override
  String toString() => "No such tool: $message";
}

/// Thrown when a tool call's input is invalid (e.g. invalid JSON or schema mismatch).
///
/// Mirrors Vercel AI SDK's `InvalidToolInputError`.
class InvalidToolInputError extends LLMError {
  final String toolName;
  final String? toolCallId;
  final String? input;
  final List<String>? validationErrors;

  InvalidToolInputError({
    required this.toolName,
    this.toolCallId,
    this.input,
    this.validationErrors,
    String message = 'Invalid tool input.',
  }) : super(message);

  @override
  String toString() {
    final parts = <String>['Invalid tool input ($toolName): $message'];
    final callId = toolCallId;
    if (callId != null && callId.isNotEmpty) {
      parts.add('toolCallId=$callId');
    }
    final errors = validationErrors;
    if (errors != null && errors.isNotEmpty) {
      parts.add('errors=${errors.join('; ')}');
    }
    return parts.join(' ');
  }
}

