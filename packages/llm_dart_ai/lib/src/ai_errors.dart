  const NoOutputGeneratedError({String message = 'No output generated.'})
      : super(message);

  @override
  String toString() => 'No output generated: $message';
}

/// Thrown when no object could be generated or validated.
///
/// This mirrors Vercel AI SDK's `NoObjectGeneratedError` concept.
class NoObjectGeneratedError extends LLMError {
  /// The raw text that was generated by the model (best-effort).
  final String? text;

  /// Best-effort finish reason, when available.
  final LLMFinishReason? finishReason;

  /// Best-effort usage snapshot, when available.
  final UsageInfo? usage;

  /// Best-effort response metadata, when available.
  final LLMResponseMetadataPart? responseMetadata;

  /// Original cause for debugging (optional).
  final Object? cause;

  const NoObjectGeneratedError({
    String message = 'No object generated.',
    this.text,
    this.finishReason,
    this.usage,
    this.responseMetadata,
    this.cause,
  }) : super(message);

  @override
  String toString() =>
      'No object generated: $message${cause == null ? '' : ' (cause: $cause)'}';
}

